// D2 real-stack scaffolder (zero-dep). From the app's backend SHAPE (auth/db/
// payments/storage/email), pick managed services, and write `.env.example` +
// `STACK.md` — the contract the build agents integrate against and the user fills
// with real keys at deploy time. Deterministic; no secrets, no network.
//
// Usage:  node stack-scaffold.mjs '<spec.json | ->'
//   spec = { "needs": { "auth":true, "db":true, "payments":false, "storage":false, "email":false },
//            "services": { "auth-db":"supabase", "payments":"stripe", "email":"resend" }   // optional overrides
//            "outDir": "." }
// Writes: <outDir>/.env.example · <outDir>/STACK.md   Exit: 0 ok · 2 bad spec

import fs from "node:fs";
import path from "node:path";

// service catalogue — env vars (server-only marked), where to get them, integration note
const CATALOG = {
  supabase: { label: "Supabase (Postgres + Auth + Storage)", covers: ["auth", "db", "storage"],
    env: [["SUPABASE_URL", false], ["SUPABASE_ANON_KEY", false], ["SUPABASE_SERVICE_ROLE_KEY", true]],
    where: "supabase.com → Project → Settings → API", note: "Use the JS client; RLS on by default; server actions use the service-role key." },
  clerk: { label: "Clerk (Auth)", covers: ["auth"],
    env: [["CLERK_PUBLISHABLE_KEY", false], ["CLERK_SECRET_KEY", true]],
    where: "dashboard.clerk.com → API Keys", note: "Drop-in auth components; protect server routes with the secret key." },
  stripe: { label: "Stripe (Payments)", covers: ["payments"],
    env: [["STRIPE_PUBLISHABLE_KEY", false], ["STRIPE_SECRET_KEY", true], ["STRIPE_WEBHOOK_SECRET", true]],
    where: "dashboard.stripe.com → Developers → API keys (+ a webhook endpoint)", note: "Checkout/Billing; verify webhooks with the signing secret; test mode first." },
  resend: { label: "Resend (Email)", covers: ["email"],
    env: [["RESEND_API_KEY", true]], where: "resend.com → API Keys", note: "Transactional email; verify your sending domain." },
  s3: { label: "S3-compatible storage", covers: ["storage"],
    env: [["S3_ENDPOINT", true], ["S3_ACCESS_KEY", true], ["S3_SECRET_KEY", true], ["S3_BUCKET", false]],
    where: "your S3/R2 provider", note: "Use a presigned-URL upload flow." },
};
const DEFAULTS = { "auth-db": "supabase", auth: "supabase", db: "supabase", payments: "stripe", email: "resend", storage: "supabase" };

const arg = process.argv[2];
if (!arg) { console.error("usage: stack-scaffold.mjs '<spec.json|->'"); process.exit(2); }
let raw = ""; try { raw = arg === "-" ? fs.readFileSync(0, "utf8") : fs.readFileSync(arg, "utf8"); } catch { try { raw = arg; } catch {} }
let spec; try { spec = JSON.parse(raw); } catch { console.error("bad spec JSON"); process.exit(2); }
const needs = spec.needs || {};
const overrides = spec.services || {};
const outDir = path.resolve(process.cwd(), spec.outDir || ".");

// choose services covering the needs (one provider can cover several)
const chosen = new Map();
function pick(need, prefer) {
  const svc = overrides[need] || prefer || DEFAULTS[need];
  if (!svc || !CATALOG[svc]) return;
  chosen.set(svc, CATALOG[svc]);
}
if (needs.auth || needs.db) pick("auth-db", overrides["auth-db"]);   // supabase covers both
if (needs.auth && !needs.db && !chosen.size) pick("auth");
if (needs.storage && ![...chosen.values()].some((s) => s.covers.includes("storage"))) pick("storage");
if (needs.payments) pick("payments");
if (needs.email) pick("email");

if (!chosen.size) {
  console.log("No backend services needed (static app) — D1 path applies; nothing scaffolded.");
  process.exit(0);
}

// .env.example
const envLines = ["# Generated by stack-scaffold — fill with REAL keys at deploy time. NEVER commit real values.", ""];
for (const [id, s] of chosen) { envLines.push(`# ${s.label} — ${s.where}`); s.env.forEach(([k, server]) => envLines.push(`${k}=${server ? "" : ""}${server ? "   # SERVER-ONLY (never expose to the client)" : ""}`)); envLines.push(""); }
fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(path.join(outDir, ".env.example"), envLines.join("\n"));

// STACK.md
const S = ["# STACK — managed services", "", "Chosen to cover the app's backend shape. Build agents integrate against these; you supply real keys at deploy.", ""];
S.push("| Need | Service | Env vars | Where |", "|---|---|---|---|");
for (const [id, s] of chosen) S.push(`| ${s.covers.join(", ")} | ${s.label} | ${s.env.map(([k, sv]) => "`" + k + "`" + (sv ? "🔒" : "")).join(" ")} | ${s.where} |`);
S.push("", "_🔒 = server-only secret (never ship to the client)._", "", "## Integration notes");
for (const [id, s] of chosen) S.push(`- **${s.label}** — ${s.note}`);
S.push("", "## Secrets", "- Real keys go in deploy env (host UI) — NOT in the repo. `.env.example` lists what's needed.", "- `protect-state` hook + `scan-surface` keep keys out of tracked files.");
fs.writeFileSync(path.join(outDir, "STACK.md"), S.join("\n") + "\n");

console.log(`stack: ${[...chosen.values()].map((s) => s.label).join(" · ")}`);
console.log(`wrote ${path.join(outDir, ".env.example")} + ${path.join(outDir, "STACK.md")}`);
console.log(`env vars: ${[...chosen.values()].flatMap((s) => s.env.map(([k]) => k)).join(", ")}`);
process.exit(0);
